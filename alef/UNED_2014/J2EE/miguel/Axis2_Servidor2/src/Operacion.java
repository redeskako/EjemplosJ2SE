
public class Operacion {

	
	public void sumar (int a, int b){
		int resultado;
		resultado = a+b;
		System.out.println("La suma de "+String.valueOf(a) +" y "+String.valueOf(b) + " es :"+String.valueOf(resultado)) ;
	}
	
	public void restar (int a, int b){
		int resultado;
		resultado = a-b;
		System.out.println("La resta de "+String.valueOf(a) +" y "+String.valueOf(b) + " es :"+String.valueOf(resultado)) ;
	}
	
	
}
